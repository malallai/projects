#include <strings.h>

void	ft_bzero(void *s, size_t n)
{
	int				i;
	unsigned char	*tmp;

	i = 0;
	tmp = s;
	while (i < (int)n)
	{
		tmp[i] = 0;
		i++;
	}
}
